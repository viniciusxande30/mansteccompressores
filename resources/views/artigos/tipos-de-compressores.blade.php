            <h4>1. Compressor de Pistão (Alternativo)</h4>
            <p>
              O compressor de pistão é um dos modelos mais tradicionais do mercado. Ele funciona por meio de movimento alternativo, comprimindo o ar em ciclos.
            </p>

            <ul>
              <li>Indicado para uso intermitente</li>
              <li>Menor custo inicial</li>
              <li>Ideal para pequenas demandas</li>
              <li>Maior nível de ruído</li>
              <li>Menor eficiência em uso contínuo</li>
            </ul>

            <h4>2. Compressor de Parafuso</h4>
            <p>
              O compressor de parafuso é o mais utilizado na indústria moderna. Ele trabalha de forma contínua e oferece maior eficiência energética.
            </p>

            <ul>
              <li>Operação contínua e estável</li>
              <li>Alta eficiência energética</li>
              <li>Menor nível de ruído</li>
              <li>Maior vida útil</li>
              <li>Ideal para médias e grandes indústrias</li>
            </ul>

            <h4>3. Compressor com Inversor de Frequência (VSD)</h4>
            <p>
              Esse tipo de compressor ajusta automaticamente sua velocidade conforme a demanda de ar, evitando desperdício de energia.
            </p>

            <ul>
              <li>Máxima eficiência energética</li>
              <li>Redução significativa no consumo de energia</li>
              <li>Ideal para demandas variáveis</li>
              <li>Maior investimento inicial com retorno rápido</li>
            </ul>

            <h4>4. Compressor Isento de Óleo</h4>
            <p>
              Indicado para aplicações que exigem ar comprimido extremamente limpo, como indústrias alimentícias, farmacêuticas e hospitalares.
            </p>

            <ul>
              <li>Ar 100% livre de óleo</li>
              <li>Alta qualidade do ar</li>
              <li>Maior custo operacional</li>
              <li>Necessário em aplicações específicas</li>
            </ul>

