            <h4>1. Calcule a vazão necessária (m³/min ou PCM)</h4>
            <p>
              A vazão representa o volume de ar que o sistema precisa fornecer. Para calcular, é necessário somar o consumo de todos os equipamentos pneumáticos.
            </p>

            <ul>
              <li>Identifique cada equipamento que utiliza ar comprimido</li>
              <li>Verifique o consumo individual (manual ou fabricante)</li>
              <li>Some todos os consumos</li>
              <li>Adicione uma margem de segurança de 20% a 30%</li>
            </ul>

            <h4>2. Defina a pressão de trabalho (bar ou psi)</h4>
            <p>
              A pressão deve atender o equipamento mais exigente da operação. Trabalhar com pressão acima do necessário gera desperdício de energia.
            </p>

            <ul>
              <li>Identifique o equipamento que exige maior pressão</li>
              <li>Adicione margem para perdas na linha</li>
              <li>Evite superdimensionamento de pressão</li>
            </ul>

            <h4>3. Analise o regime de operação</h4>
            <p>
              Entender se o uso será contínuo ou intermitente é essencial para definir o tipo de compressor ideal.
            </p>

            <ul>
              <li>Uso contínuo → compressor de parafuso</li>
              <li>Uso intermitente → compressor de pistão</li>
              <li>Demanda variável → compressor com inversor (VSD)</li>
            </ul>

            <h4>4. Considere picos de consumo</h4>
            <p>
              Muitas operações possuem momentos de alta demanda. Ignorar isso pode causar queda de pressão e falhas.
            </p>

            <ul>
              <li>Identifique horários de pico</li>
              <li>Verifique uso simultâneo de equipamentos</li>
              <li>Dimensione para o pior cenário</li>
            </ul>

            <h4>5. Avalie a qualidade do ar comprimido</h4>
            <p>
              Algumas aplicações exigem ar seco e livre de contaminantes, o que exige filtros e secadores específicos.
            </p>

            <ul>
              <li>Indústria alimentícia → ar isento de óleo</li>
